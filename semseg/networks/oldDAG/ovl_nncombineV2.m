function [Y] = vl_nncombineV2(X1, X2, dzdy)
	if(nargin == 2)
		% X2 = 2 --> X1 = 7
		% X2 = 3 --> X1 = 8
		% X2 = 4 --> X1 = 9
		% X2 = 5 --> X1 = 10
		% X2 = 6 --> X1 = 11
		Y = gpuArray.zeros(size(X1), 'single');
		Y(:,:,1, :) = X1(:,:,1,:) + X2(:,:,1,:);
		Y(:,:,2, :) = X1(:,:,2,:) + X2(:,:,1,:);
		Y(:,:,3, :) = X1(:,:,3,:) + X2(:,:,1,:);
		Y(:,:,4, :) = X1(:,:,4,:) + X2(:,:,1,:);
		Y(:,:,5, :) = X1(:,:,5,:) + X2(:,:,1,:);
		Y(:,:,6, :) = X1(:,:,6,:) + X2(:,:,1,:);
		Y(:,:,7, :) = X1(:,:,7,:) + X2(:,:,2,:);
		Y(:,:,8, :) = X1(:,:,8,:) + X2(:,:,3,:);
		Y(:,:,9, :) = X1(:,:,9,:) + X2(:,:,4,:);
		Y(:,:,10, :) = X1(:,:,10,:) + X2(:,:,5,:);
		Y(:,:,11, :) = X1(:,:,11,:) +  X2(:,:,6,:);
	else
		Y = cell(1, 2);
		Y{1} = gpuArray.zeros(size(X1), 'single');
		Y{2} = gpuArray.zeros(size(X2), 'single');
	end
end
